n1 = int(input("Ingresa el 1er valor: "))
n2 = int(input("Ingresa el 2do valor: "))
print("{}^{} = {}".format(n1, n2, n1**n2))
print("{}/{} = {}".format(n1, n2, n1//n2))
print("{} mod {} = {}".format(n1, n2, n1%n2))
print("{} < {} = {}".format(n1, n2, n1<n2))
print("{} > {} = {}".format(n1, n2, n1>n2))
print("{} >= {} = {}".format(n1, n2, n1>=n2))
print("{} <= {} = {}".format(n1, n2, n1<=n2))
print("{} == {} = {}".format(n1, n2, n1==n2))
print("{} != {} = {}".format(n1, n2, n1!=n2))